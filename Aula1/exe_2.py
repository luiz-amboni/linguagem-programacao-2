class EscreveNumero:

    num = int(input())
    if num == 0:
        print("Zero")
    elif num == 1:
        print("Um")
    elif num == 2:
        print("Dois")
    elif num == 3:
        print("Tres")
    elif num == 4:
        print("Quatro")
    elif num == 5:
        print("Cinco")
    else:
        print("Número Inválido")